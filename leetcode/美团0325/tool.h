#include "stdc++.h"
template<typename T> void printArr(T nums[], int count){
    for (size_t i = 0; i < count; i++)
    {
        cout << nums[i] << endl;
    }
    
}
template<typename T> void printArr2(vector<T> nums){
    for (size_t i = 0; i < nums.size(); i++)
    {
        cout << nums[i] << endl;
    }
}